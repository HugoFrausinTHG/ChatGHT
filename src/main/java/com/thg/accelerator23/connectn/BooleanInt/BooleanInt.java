package com.thg.accelerator23.connectn.BooleanInt;

public class BooleanInt{
    public boolean aBoolean;
    public int anInt;
    public BooleanInt(boolean aBoolean, int anInt){
        this.aBoolean = aBoolean;
        this.anInt = anInt;
    }

    public int getAnInt() {
        return anInt;
    }
    public boolean getaBoolean() {
        return this.aBoolean;
    }
}